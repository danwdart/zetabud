	/**
	 * Sets a field from the object by Position as specified in the xml schema.
	 * Zero-based.
	 *
	 * @param      int $pos position in xml schema
	 * @param      mixed $value field value
	 * @return     void
	 */
	public function setByPosition($pos, $value)
	{
		switch($pos) {
			case 0:
				$this->setId($value);
				break;
			case 1:
				$this->setUser1Id($value);
				break;
			case 2:
				$this->setUser2Id($value);
				break;
			case 3:
				$this->setFriendGroupId($value);
				break;
			case 4:
				$this->setRequestedDate($value);
				break;
			case 5:
				$this->setConfirmedDate($value);
				break;
		} // switch()
	}

	/**
	 * Populates the object using an array.
	 *
	 * This is particularly useful when populating an object from one of the
	 * request arrays (e.g. $_POST).  This method goes through the column
	 * names, checking to see whether a matching key exists in populated
	 * array. If so the setByName() method is called for that column.
	 *
	 * You can specify the key type of the array by additionally passing one
	 * of the class type constants BasePeer::TYPE_PHPNAME, BasePeer::TYPE_STUDLYPHPNAME,
	 * BasePeer::TYPE_COLNAME, BasePeer::TYPE_FIELDNAME, BasePeer::TYPE_NUM.
	 * The default key type is the column's phpname (e.g. 'AuthorId')
	 *
	 * @param      array  $arr     An array to populate the object from.
	 * @param      string $keyType The type of keys the array uses.
	 * @return     void
	 */
	public function fromArray($arr, $keyType = BasePeer::TYPE_PHPNAME)
	{
		$keys = FriendPeer::getFieldNames($keyType);

		if (array_key_exists($keys[0], $arr)) $this->setId($arr[$keys[0]]);
		if (array_key_exists($keys[1], $arr)) $this->setUser1Id($arr[$keys[1]]);
		if (array_key_exists($keys[2], $arr)) $this->setUser2Id($arr[$keys[2]]);
		if (array_key_exists($keys[3], $arr)) $this->setFriendGroupId($arr[$keys[3]]);
		if (array_key_exists($keys[4], $arr)) $this->setRequestedDate($arr[$keys[4]]);
		if (array_key_exists($keys[5], $arr)) $this->setConfirmedDate($arr[$keys[5]]);
	}

	/**
	 * Build a Criteria object containing the values of all modified columns in this object.
	 *
	 * @return     Criteria The Criteria object containing all modified values.
	 */
	public function buildCriteria()
	{
		$criteria = new Criteria(FriendPeer::DATABASE_NAME);

		if ($this->isColumnModified(FriendPeer::ID)) $criteria->add(FriendPeer::ID, $this->id);
		if ($this->isColumnModified(FriendPeer::USER1_ID)) $criteria->add(FriendPeer::USER1_ID, $this->user1_id);
		if ($this->isColumnModified(FriendPeer::USER2_ID)) $criteria->add(FriendPeer::USER2_ID, $this->user2_id);
		if ($this->isColumnModified(FriendPeer::FRIEND_GROUP_ID)) $criteria->add(FriendPeer::FRIEND_GROUP_ID, $this->friend_group_id);
		if ($this->isColumnModified(FriendPeer::REQUESTED_DATE)) $criteria->add(FriendPeer::REQUESTED_DATE, $this->requested_date);
		if ($this->isColumnModified(FriendPeer::CONFIRMED_DATE)) $criteria->add(FriendPeer::CONFIRMED_DATE, $this->confirmed_date);

		return $criteria;
	}

	/**
